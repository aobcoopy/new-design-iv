    <div class="col-md-12 nopad top41 f18">
                There’s no doubting that Sri Lanka is one of the world’s hottest destinations right now, and Galle is one of the most beautiful parts of the island. If you’re looking for an area that is rich in culture, natural beauty and history, you really can’t go past it.
        
         <br><br>
        Galle is situated on the south coast of Sri Lanka, about three hours’ from the capital of Colombo, which is where most visitors fly into. There’s no need to worry about the long transfer to Galle, though — the journey weaves along Sri Lanka’s stunning coast and is a wonderful experience in itself.
        <br><br>
         
        Another (equally delightful) option is to get the train, which takes in the same beautiful views as the road, and has a certain added charm of yesteryear.
        <br><br>
         
        It’s a place you can’t help but fall in love with.
        <br><br>
         
        Perhaps the most famous attraction in Galle is the sixteenth century fort, a throwback from when the area was an important trading port. <br><br>
        
        History buffs will love learning about the different periods in the area’s history, and everyone can appreciate the sheer beauty of the fort and its surroundings.
        <br><br>
         
        Similarly, simple meandering through the beautiful city of Galle is a delight in itself. The entire town is UNESCO World Heritage listed, and it’s not hard to see why. Galle is a maze of laneways lined with historic colonial buildings, museums and foodie hotspots.
        <br><br>
         
        There are an increasing number of cafes and restaurants here. The cooking traditions of Galle date back centuries, with various influences including Portuguese, Dutch, Indian and English. <br><br>
        
        Thanks to its location on the coast, sea food in particular is delicious in Galle; with grilled calamari and seared tuna two particular delicacies.
        <br><br>
         
        Overall, Galle certainly deserves its reputation as the culinary hotspot of Sri Lanka, and whether you’re looking for a bohemian cafe or an upscale restaurant, you’re sure to find it. Or, you could always request a private chef to prepare you a delicious meal in your villa.
        <br><br>
         
        If you are in Galle on a Sunday, then you can’t miss the Galle Fort Market, where there are many different food stalls offering incredible traditional snacks and dishes. There is also live music which allows another fascinating glimpse into the culture.
        <br><br>
         
        Shopping is another excellent way to spend an afternoon in Galle. The area is particularly popular with local and foreign artists, such as photographers, painters and writers, and many have set up delightful shops and boutiques. There is a seemingly endless choice for stylish and unique souvenirs of your stay in Sri Lanka’s south.
        <br><br>
         
        Perhaps the biggest draw to Galle is its stunning beaches — in fact, many of the villas for rent in Galle anhd the south coast are beachfront, allowing you to fully enjoy the beauty infront of you. <br><br>
        
        One of the most beautiful beaches is Unawatuna, which is a postcard-perfect beach with a swathe of soft sand fringed by palm trees. The beach is beautiful at all times, but is particularly magical as the sun dips or rises above the clear waters.
        <br><br>
         
        The water is also good for swimming. and surfing is particularly popular on Unawatuna. It’s a great place to learn to surf, as the waves are gentle and consistent, and there are several surf schools in the area. <br><br>
        
        Another fun activity to enjoy is going out on a boat trip — in whale watching season, you may spot some of the sea’s largest mammals, while inquisitive dolphins are playful all year round.
        <br><br>
         
        For further relaxation, you may like to enjoy a relaxing and restorative massage. There are excellent spas and massage parlours in Galle, which offer many different types of massages and spa treatments. Many of the treatments have routes in healing practices that have been performed in Sri Lanka for centuries.
        
         <br><br>
        While the popularity of Sri Lanka continues to skyrocket, Galle remains somewhat under-the-radar, meaning it is a relaxing and authentic place to explore. <br><br>
        
        Of course, with so much to offer in terms of cuisine, attractions and shopping, it is only a matter of time before everyone discovers the secret. Therefore, there’s no better time to enjoy the magical beauty of Galle and southern Sri Lanka than right now.
        
        
        
        
        
